package operations

// Go Function syntax
// func <function_name> (<parameter name> <parameter type>) <return type> {
// 	<code>
// }

func Add(a, b int) int {
	return a + b
}

func Minus(a, b int) int {
	return a - b
}

func Multiply(a, b int) int {
	return a - b
}

func Divide(a, b int) float32 {
	return float32(a) / float32(b)
}
